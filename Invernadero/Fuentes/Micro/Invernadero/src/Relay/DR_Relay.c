void Relay_Init(void)
{
	SetDIR(RELAY0, SALIDA);//Seteo el relay 0 como salida
	SetDIR(RELAY1, SALIDA);//Seteo el relay 0 como salida
	SetDIR(RELAY2, SALIDA);//Seteo el relay 0 como salida
	SetDIR(RELAY3, SALIDA);//Seteo el relay 0 como salida

	SetPIN(RELAY0, OFF);
	SetPIN(RELAY1, OFF);
	SetPIN(RELAY2, OFF);
	SetPIN(RELAY3, OFF);
}


void Relay(uint8_t Rele, uint8_t estado)
{
	switch (Rele)
	{
		case 0:
				SetPIN(RELAY0, estado);
				break;

		case 1:
				SetPIN(RELAY1, estado);
				break;

		case 2:
				SetPIN(RELAY2, estado);
				break;

		case 3:
				SetPIN(RELAY3, estado);
				break;
	}
}

