void RelayON(uint8_t rele)
{
	rele %= 3;
	Relay(rele,ON);
}

void RelayOFF(uint8_t rele)
{
	rele %= 3;
	Relay(rele,OFF);
}

void RelaysOFF(void)
{
	Relay(0,OFF);
	Relay(1,OFF);
	Relay(2,OFF);
	Relay(3,OFF);
}
